class Calculadora {
    fun soma(val1: Int, val2: Int): Int {
        return val1 + val2
    }

    fun subtracao(val1: Int, val2: Int): Int {
        return val1 - val2
    }

    fun multiplicacao(val1: Int, val2: Int): Int {
        return val1 * val2
    }

    fun divisao(val1: Int, val2: Int): Int {
        return val1 / val2
    }
}